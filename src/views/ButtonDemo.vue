<template>
  <div>
    <h2>Standard buttons</h2>
    <example
      :code="standard"
      :data="{clicked: 0}"
      max-width="12rem"
    />

    <h2>Danger button</h2>
    <example
      :code="`<s-button danger>Delete</s-button>`"
      max-width="12rem"
    />

    <h2>Inactive buttons</h2>
    <example
      :code="inactive"
      max-width="12rem"
    />

    <h2>Smaller version</h2>
    <example
      :code="smaller"
      max-width="10rem"
    />

    <h2>Props</h2>
    <props-list>
      <props-item
        name="small"
        type="Boolean"
      >
        Shows the small version.
      </props-item>
      <props-item
        name="primary"
        type="Boolean"
      >
        Shows the primary version.
      </props-item>
      <props-item
        name="danger"
        type="Boolean"
      >
        Shows the danger version.
      </props-item>
      <props-item
        name="inactive"
        type="Boolean"
      >
        Shows the button in an inactive state.
      </props-item>
      <props-item
        name="submit"
        type="Boolean"
      >
        Sets the button type to submit.
      </props-item>
    </props-list>
  </div>
</template>

<script lang="ts">
import Vue from 'vue';

export default Vue.extend({
  name: 'ButtonDemo',

  computed: {
    standard() {
      return `\
  <s-button @click="clicked -= 1">Normal button</s-button>
  <s-button primary @click="clicked += 1">Primary button</s-button>`;
    },

    inactive() {
      return `\
  <s-button inactive>Secondary inactive</s-button>
  <s-button primary inactive>Primary inactive</s-button>`;
    },

    smaller() {
      return `\
  <s-button small>Secondary</s-button>
  <s-button small primary>Primary</s-button>`;
    },
  },
});
</script>
